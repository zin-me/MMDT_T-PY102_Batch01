# Q1. is_balanced_parentheses (s: str)
"""

def is_balanced_parentheses(self, s: str) -> bool:
        pair_dict = {")": "(", "}": "{", "]": "["}
        stack_list = []

        for char in s:
            if char in "({[":
                stack_list.append(char)
            elif char in ")}]":
                if not stack_list or stack_list.pop() != pair_dict[char]:
                    return False
        return len(stack_list) == 0

"""# Q2. next_greater_to_right (nums: list[int])"""

def next_greater_to_right(nums: list[int]) -> list[int]:
  result=[-1]*len(nums)
  stack=[]

  for i in range(len(nums)-1,-1,-1):
    while stack and stack[-1]<=nums[i]:
      stack.pop()

    if stack:
      result[i]=stack[-1]

    stack.append(nums[i])

  return result

