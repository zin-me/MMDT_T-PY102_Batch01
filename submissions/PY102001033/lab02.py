"""# Q4. hot_potato(names: list[str], k: int)"""

def hot_potato(names: list[str], k: int) -> str:
  queue=deque(names)
  while len(queue)>1:
    for _ in range(k):
      queue.append(queue.popleft())

    queue.popleft()

  return queue[0]